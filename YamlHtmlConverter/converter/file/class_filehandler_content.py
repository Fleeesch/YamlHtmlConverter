class Content:

    # ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
    #   Constructor
    # ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~

    def __init__(self, lines: list[str]):
        self.lines = lines

        # remove linebreak
        for idx, line in enumerate(self.lines):
            self.lines[idx] = line.replace('\n', '')

        # store highest index number
        self.max_lines = len(self.lines) - 1
